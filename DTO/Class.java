package DTO;

public class Class {
	private String classid;
	private String classname;
	private String trainsystem;
	private String yearofaddmission;
	private int wholesalenumber;
	public String getClassid() {
		return classid;
	}
	public void setClassid(String classid) {
		this.classid = classid;
	}
	public String getClassname() {
		return classname;
	}
	public void setClassname(String classname) {
		this.classname = classname;
	}
	public String getTrainsystem() {
		return trainsystem;
	}
	public void setTrainsystem(String trainsystem) {
		this.trainsystem = trainsystem;
	}
	public String getYearofaddmission() {
		return yearofaddmission;
	}
	public void setYearofaddmission(String yearofaddmission) {
		this.yearofaddmission = yearofaddmission;
	}
	public int getWholesalenumber() {
		return wholesalenumber;
	}
	public void setWholesalenumber(int wholesalenumber) {
		this.wholesalenumber = wholesalenumber;
	}
	

}
